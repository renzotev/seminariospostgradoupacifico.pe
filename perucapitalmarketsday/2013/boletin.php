                                <h1 class="accordion_toggle">Luis Hernández: "Como reguladores tratamos de generar las mejores pensiones ..."</h1>
                                <div class="accordion_content">
                                        <div class="title-article">Luis Hernández: "Como reguladores tratamos de generar las mejores pensiones para los trabajadores"</div>
                                        <br>                                        
                                        <br>
                                        <div class="fotoarticulo">
                                            <img src="/img/perucapitalmarketsday/2012/boletin/accordion_2.png">
                                        </div>  
                                    <p>
                                        Como parte del primer panel del Peru Capital Markets Day 2012 se realizó la conferencia Las Oportunidades de Inversión Luego de la Reforma de los Fondos de Pensiones, la cual estuvo a cargo de Luis Mario Hernández, Director General de Supervisión de Inversiones y Riesgos de la Comisión Nacional del Sistema para el Ahorro y el Retiro (CONSAR - México)
                                    </p>        

                                    <p>
                                        Hernández destacó en primer término, la función que cumple la CONSAR en México. "Como reguladores tratamos de generar las mejores pensiones para los trabajadores. Esto significa que tenemos que cuidar que el capital se conserve, para que haya un régimen de inversión flexible y que las AFORES aprovechen estas oportunidades para que se genere una rentabilidad  sosteniblemente alta", destacó.
                                    </p>                                        

                                    <p>
                                        Además comentó sobre el espectacular resultado que generan los rendimientos dentro de los saldos acumulados. "Si los saldos acumulados los descomponemos entre rendimientos y aportaciones, los rendimientos son un porcentaje mucho más alto (42% del total). En el largo plazo se espera un crecimiento mucho mayor en este sentido", finalizó Hernández.
                                    </p>
                                    <p>
                                        Cabe resaltar que como parte del Panel 1 participaron Jorge Ramos Raygada, Gerente General de AFP Integra; Ricardo Mogrovejo, Gerente de Inversiones de AFP Capital de Chile; Juan Camilo Osorio, Gerente de Inversiones de AFP Protección de Colombia; y Rafael Carranza, Gerente General de AFP Horizonte. El moderador fue Melvin Escudero, CEO de El Dorado Investments y Director de la Maestría en Finanzas de la Universidad del Pacífico.
                                    </p>                                    
                                </div>  
                                
                                <h1 class="accordion_toggle">Clemente del Valle: "Las necesidades están claras para desarrollar un mercado de capitales"</h1>
                                <div class="accordion_content">
                                        <div class="title-article">Clemente del Valle: "Las necesidades están claras para desarrollar un mercado de capitales"</div>
                                        <br>                                        
                                        <br>
                                        <div class="fotoarticulo">
                                            <img src="/img/perucapitalmarketsday/2012/boletin/accordion_3.png">
                                        </div>  
                                    <p>
                                        El segundo Panel del Peru Capital Markets Day 2012 presentó a Clemente del Valle, Representante del Banco Mundial - ICF de Colombia. Del Valle abordó el tema referente a las oportunidades de Inversión luego de la reforma del Mercado de Capitales.
                                    </p>        

                                    <p>
                                        Clemente del Valle estuvo acompañado por Aldo Ferrini, Gerente Central de Inversiones de AFP integra; Felipe Morris, Presidente del Directorio de Interseguro Compañía de Seguros; Eduardo Gómez de la Torre, Gerente de Finanzas Corporativas del Banco de Crédito del Perú; y Francis Stenning, Gerente General de la Bolsa de Valores de Lima. El panel fue moderado por Guillermo Garrido-Lecca, Gerente General Adjunto de Pacífico Seguro.
                                    </p>

                                    <p>
                                        "Las crisis de los últimos 15 años han reforzado y han generado consciencia en los gobiernos sobre la necesidad de impulsar la agenda del mercado de capitales, pero toda la reforma que se ha dado se ha convertido en un gran motor para elevar la agenda del mercado de capitales en todos los países emergentes" señaló Del Valle. 
                                    </p>

                                    <p>
                                        Es conocido que existen diferentes problemas en la reforma del mercado de Capitales. Clemente del Valle ha detectado dos por solucionar que son fundamentales. "El primero es el desbalance entre la oferta y la demanda. El otro es la poca profundización que tiene el mercado de capitales en comparación con otros países, lo cual limita el crecimiento de este tipo de vehículos de transformación", comentó.
                                    </p>

                                    <p>
                                        Por último, Clemente del Valle reafirmó que las condiciones para el desarrollo de la reforma están dadas. "Las necesidades están claras para desarrollar un mercado de capitales, existe un entorno positivo, una situación económica estable y sólida. Hay una necesidad muy grande de mejorar el retorno de un ahorro creciente y veo al gobierno comprometido con esta agenda", expresó. 
                                    </p>

                                    <p>
                                        "Están las condiciones dadas para que entre gobierno y sector privado construyan esa agenda para una reforma de fondo y comprensiva del mercado de capitales", finalizó Clemente del Valle.
                                    </p>                                    

                                </div>                                  

                                <h1 class="accordion_toggle">David Saettone: "Los beneficios del Mercado de Capitales en el Perú llegan a pocas empresas"</h1>
                                <div class="accordion_content">
                                        <div class="title-article">David Saettone: "Los beneficios del Mercado de Capitales en el Perú llegan a muy pocas empresas"</div>
                                        <br>                                        
                                        <br>
                                        <div class="fotoarticulo">
                                            <img src="/img/perucapitalmarketsday/2012/boletin/accordion_4.png">
                                        </div>  
                                    <p>
                                        David Saettone, Gerente General de Pacífico Seguros, fue el expositor principal en el tercer panel del Peru Capital Markets Day 2012. A su cargo estuvo la exposición sobre "Los Mercados de Renta Fija y Bonos: Oportunidades en el Mercado Local e Internacional".
                                    </p>        

                                    <p>
                                        Los panelistas que acompañaron a David Saettone fueron Luis de la Cerda, Director de Inversiones de AFORE SURA de México; Víctor Díaz, Gerente de la Mesa de Inversiones de Prima AFP; Wilfried Marchand, Director de Renta Fija del BNP Paribas de USA; y Hugo Horta, DCM Head de IM Trust. Como moderador estuvo Gerardo Luengas, Gerente General de PiP Latam en México.
                                    </p>    

                                    <p>
                                        David Saettone destacó el atractivo de los mercados emergentes, especialmente en esta coyuntura internacional. "Hemos tenido dos décadas de crecimiento económico donde las economías emergentes han crecido mucho más que la desarrolladas y esto ha generado una atracción tremenda en lo que es inversión extranjera directa donde las economías en desarrollo están atrayendo mucho más inversiones, mucho más flujo de capital", destacó.
                                    </p>    

                                    <p>
                                        El crecimiento económico del país ha sido fundamental para el desarrollo del mercado de Capitales y el incremento de las empresas en él. "A raíz del crecimiento económico, en los últimos cinco años el Perú, donde en el 2006 habían 70 empresas que vendían más de 500 millones de soles y en el 2011 tenemos 178. Eso quiere decir que tenemos una mayor base de empresas que están listas para emitir en el mercado de capitales", afirmó Saettone.
                                    </p>    

                                    <p>
                                        Por último, David Saettone resaltó el espacio que tiene el mercado de capitales para continuar con su crecimiento. "En el Perú, el mercado de capitales tiene un gran mercado en el cual desarrollarse, pero sus beneficios llegan a muy pocas empresas. La pregunta que debemos hacernos es qué debemos hacer para que esto se destrabe y tenga un mayor dinamismo", sentenció.
                                    </p>                                            

                                </div>          

                                <h1 class="accordion_toggle">Sergio Schmucker: "Hemos hecho muchas reformas con las cuales hemos logrado avanzar"</h1>
                                <div class="accordion_content">
                                        <div class="title-article">Sergio Schmucker: "Hemos hecho muchas reformas con las cuales hemos logrado avanzar"</div>
                                        <br>                                        
                                        <br>
                                        <div class="fotoarticulo">
                                            <img src="/img/perucapitalmarketsday/2012/boletin/accordion_5.png">
                                        </div>  
                                    <p>
                                        En el cuarto panel se disertó sobre las Tendencias en los Mercados de Capitales, Inversionistas Institucionales y Oportunidades de Diversificación Global. La exposición estuvo a cargo de Sergio Schmucker, Economista del Banco mundial en Estados Unidos.
                                    </p>    
                                    <p>
                                        Además esta ponencia contó con la participación de un distinguido grupo de panelistas moderados por Alonso Segura, Docente de la Escuela de Postgrado de la Universidad del Pacífico. Estuvieron presente Alejandro Pérez Reyes, Director de Gestión de Activos del BCP; Carlos Budge, Director General de Gestión de Activos del Grupo Security de Chile; José Martínez, Gerente General Adjunto de Rímac Seguros; y Carlos Montalván, Gerente General de Interfondos.
                                    </p>
                                    <p>
                                        "A pesar de que los mercados financieros se han desarrollado, estamos lejos de donde queremos estar. Hemos hecho muchas reformas con las cuales hemos logrado avanzar, pero aún no logramos el resultado esperado", señaló Schmucker.
                                    </p>
                                    <p>
                                        "El financiamiento es muy parcial y a pocas empresas. Los mercados de bonos como mercados accionarios son mercados que no son líquidos", agregó Sergio Schmucker.
                                    </p>
                                    <p>
                                        "El gran crecimiento de fondos se dio hacia fondos globales y no a fondos regionales. Mueven mucho dinero pero están invirtiendo muy pocos activos. Este patrón de inversiones no solo sucede en países en desarrollo", finalizó Sergio Schmucker.
                                    </p>

