                                <h1 class="accordion_toggle">Juan Carlos Félix: "Han sido años de altísimo crecimiento para la región"</h1>
                                <div class="accordion_content">
                                        <div class="title-article">Juan Carlos Félix: "Han sido años de altísimo crecimiento para la región"</div>
                                        <br>                                        
                                        <br>
                                        <div class="fotoarticulo">
                                            <img src="/img/perucapitalmarketsday/2012/boletin/accordion_7.png">
                                        </div>  
                                    <p>
                                        En el sexto y último panel del Peru Capital Markets Day 2012, se presentó la ponencia sobre La Oportunidad de los Mercados Privados, la disertación estuvo a cargo de Juan Carlos Félix, Managing Director de The Carlyle Group de Brasil.
                                    </p>    
                                    <p>
                                        Cabe resaltar que los panelistas en esta oportunidad fueron Javier Rodríguez, Gerente General del Grupo Wiese; Alberto Arias, Fundador y Gerente General de Arias Resources Capital Management;  Jorge Ramos Felices, Gerente General de COFIDE; y Leandro Festino, Managing Partner de Meketa Investments Group de Estados Unidos. El moderador en esta ocasión fue Víctor Pinto, Jefe de Inversiones Alternativas de Profuturo AFP.
                                    </p>
                                    <p>
                                        "Se espera que el nivel de inversiones en la región en un futuro sea mucho más grande y superar los 10 billones a los que se llegó en el 2007. Han sido años de altísimo crecimiento para la región", señaló Juan Carlos Félix.
                                    </p>
                                    <p>
                                        Además Juan Carlos Félix considera al Perú un país en el cual hay que invertir. "Consideramos el Perú como un país interesante porque creemos que existe menos competencia, claramente hay menos competencia que en Brasil, Colombia y Chile. Nos gusta entrar en el momento del desarrollo del mercado porque es la mejor oportunidad para invertir", añadió.
                                    </p>
                                    <p>
                                        Por último, Juan Carlos Félix comentó por qué invierten en el Perú. "Nosotros nos hemos especializado en trabajar juntos con los dueños de las empresas, con los fundadores y dueños de la empresa. Aquí en el Perú 80% son empresas familiares, representan 65% de la generación de empleo del país y 45% del PIB, hay mucha materia prima con que trabajar y eso es lo que nosotros estamos buscando y lo que nos gusta del Perú", sentenció.
                                    </p>                                    

