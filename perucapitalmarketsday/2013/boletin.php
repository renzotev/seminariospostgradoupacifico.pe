                                <h1 class="accordion_toggle">Luis Alfonso Carrera: "El reto para el 2013 es consolidar este vehículo regional"</h1>
                                <div class="accordion_content">
                                        <div class="title-article">Luis Alfonso Carrera: "El reto para el 2013 es consolidar este vehículo regional"</div>
                                        <br>                                        
                                        <br>
                                        <div class="fotoarticulo">
                                            <img src="/img/perucapitalmarketsday/2012/boletin/accordion_6.png">
                                        </div>  
                                    <p>
                                        En el quinto panel, se presentó el tema Las Oportunidades de Inversión que ofrece el Mercado MILA, la exposición estuvo a cargo de Luis Alfonso Carrera, Gerente de Banca de Inversión del Banco de Crédito del Perú.
                                    </p>
                                    <p>
                                        En esta oportunidad, el panel estuvo conformado por  Marco Peschiera, Gerente General de Carlyle Perú; Gonzalo Camargo, Gerente de Inversiones de AFP Provida de Chile; Axel Christensen, Managing Director Responsable de Latinoamérica ex Brasil de BlackRock de Chile; y Daniel Velandia, Gerente de Research de Correval en Colombia. EL panel estuvo moderado por Rodrigo Fuenzalida, Gerente General de Corredora de Bolsa Security de Chile.
                                    </p>
                                    <p>
                                        "El Mercado Integrado Latinoamericano (MILA) es una realidad, no desde el punto de vista normativo, pero si desde la perspectiva de las empresas, es decir las empresas chilenas, colombianas y peruanas son menos chilenas, colombianas o peruanas y más internacionales", mencionó Luis Alfonso Carrera.
                                    </p>
                                    <p>
                                        Carrera mencionó qué países componen el MILA y en qué radica su importancia. "Los componentes y los países que componen el MILA son los alumnos más aplicados de la región y destacan claramente entre cualquier otro miembro en Latinoamérica. Esto se da tanto en términos de evolución del PBI, inflación, atracción de inversión extranjera, como en términos de perspectiva de crecimiento para los próximos cinco o diez años, los mercados de Chile Colombia y Perú destacan claramente sobre el resto", agregó Carrera.
                                    </p>
                                    <p>
                                        Para finalizar, Carrera asumió el desafío para el próximo año. "El reto para el 2013 es consolidar este "vehículo" regional. Seremos una organización con más de 900 empleados administrando activos por más de 10 billones de dólares, con 35 analistas de research en tres países que cubren más de 80 empresas, más de 8,400 millones de dólares en asesorías y transacciones en el 2011; y con acceso al 100% de inversionistas relevantes en la región", sentenció Carrera.
                                    </p>                                    

