                            <h1 class="accordion_toggle">Melvin Escudero: "Nuestra misión es contribuir con propuestas en el desarrollo del mercado ..."</h1>
                                <div class="accordion_content">
                                        <div class="title-article">Melvin Escudero: "Nuestra misión es contribuir con propuestas en el desarrollo del mercado de capitales peruano."</div>
                                        <br>                                        
                                        <br>
                                        <div class="fotoarticulo">
                                            <img src="/img/perucapitalmarketsday/2012/boletin/accordion_1.png">
                                        </div>  
                                    <p>
                                        Para la Escuela de Postgrado de la Universidad del Pacífico y El Dorado Investments ha sido un honor realizar por segunda vez consecutiva el mayor evento anual destinado a contribuir con propuestas para el desarrollo del mercado de capitales peruano.
                                    </p>        

                                    <p>
                                        A este efecto, se contó con los mas importantes representantes del gobierno, reguladores, académicos, gestores de fondos de pensiones, fondos mutuos, fondos de inversión, banqueros y aseguradores, entre otros, líderes de las inversiones y finanzas; quienes  realizaron interesantes presentaciones y un nutrido intercambio de ideas con el objeto de discutir los problemas y las alternativas para que el mercado de capitales mejore en sus diferentes ámbitos de acción considerando variables de la oferta y demanda para los instrumentos emitidos por el gobierno, el mercado bursátil, los papeles de renta fija y los productos de inversión alternativos.
                                    </p>                                        

                                    <p>
                                        Entre lo más destacable estuvo el anuncio por parte del Ministro de Economía, Luis Miguel Castilla, de un detallado plan de acción sobre la implementación de la reforma del mercado de capitales que se ha comprometido a realizar y que ya viene ejecutándose en varios aspectos de manera gradual, tal como también lo comentó la Superintendente de Mercado de Valores (SMV) Lilian Rocca.
                                    </p>
                                    <p>
                                        La visión presentada por el Ministro Castilla, y que compartimos todos los que estamos involucrados con nuestro mercado de capitales, es ambiciosa y apunta en la dirección correcta: 
                                        <table>
                                          <tr>
                                            <td valign="top">></td><td>Lograr un mercado desarrollado, líquido, profundo, integrado e inclusivo que cumpla con altos estándares internacionales.</td>
                                            </tr>
                                          <tr>
                                            <td valign="top">></td><td>Tener un mercado de valores como principal fuente de financiamiento empresarial de mediano y largo plazo.</td>
                                          </tr>
                                          <tr>
                                            <td>></td><td>Incrementar la oferta de instrumentos diversificada por emisores y riesgos.</td>
                                          </tr>
                                          <tr>
                                            <td>></td><td>Mejorar la curva de rendimiento del gobierno  para que sea una referencia líquida y continua.</td>
                                          </tr>
                                        </table>
                                    </p>        
                                    <p>A tal efecto, las líneas maestras de dicha reforma que se está trabajando e implementando incluyen:
                                        <table>
                                          <tr>
                                            <td valign="top">></td><td>Flexibilizar requisitos de emisión. Creación de nuevos canales al mercado profesional.</td>
                                            </tr>
                                          <tr>
                                            <td valign="top">></td><td>Ampliar alternativas de inversión. Ingreso de pequeñas y medianas empresas.</td>
                                          </tr>
                                          <tr>
                                            <td>></td><td>Listado de empresas públicas.</td>
                                          </tr>
                                          <tr>
                                            <td valign="top">></td><td>Facilitar acceso de nuevos fondos mutuos y fondos de inversión.</td>
                                          </tr>
                                          <tr>
                                            <td valign="top">></td><td>Mejorar el gobierno corporativo en las empresas.</td>
                                          </tr>                                       
                                          <tr>
                                            <td valign="top">></td><td>Internacionalizar el mercado de capitales y promover el MILA.</td>
                                          </tr>                                       
                                        </table>                                    
                                    </p>
                                    <p>Asimismo, y de manera complementaria a dichas medidas, el Superintendente Adjunto de Seguros y AFP, Michel Canta, anunció la mejora del marco normativo de las inversiones de las AFP, quienes son los mayores inversionistas del mercado de capitales peruano con más del 50% de la demanda potencial de productos, la cual estará basada en los siguientes lineamientos:
                                        <table>
                                          <tr>
                                            <td valign="top">></td><td>Estimular la inversión y generar un incremento en el nivel de actividad económica con el consiguiente efecto positivo sobre la producción y el empleo, favoreciendo el aumento del número de afiliados y el incremento del ingreso per cápita, mejorando la capacidad de consumo y ahorro de las familias que contribuya a generar un círculo virtuoso a través del mercado de capitales</td>
                                            </tr>
                                          <tr>
                                            <td valign="top">></td><td>Facilitar la optimización de las carteras administradas para lograr mejores pensiones futuras, ampliando las alternativas de inversión y fomentando una mayor diversificación.</td>
                                          </tr>
                                          <tr>
                                            <td valign="top">></td><td>Agilizar y flexibilizar el proceso de registro de inversiones en activos simples, transfiriendo responsabilidades a las AFP.</td>
                                          </tr>
                                          <tr>
                                            <td valign="top">></td><td>Proveer a las AFP de herramientas para manejar adecuadamente los riesgos externos e internos de las carteras administradas, fomentando mayor diversificación.</td>
                                          </tr>
                                          <tr>
                                            <td valign="top">></td><td>Fomentar la adopción de mejores prácticas y estándares internacionales en el proceso de inversión. </td>
                                          </tr>                                       
                                          <tr>
                                            <td valign="top">></td><td>Mejorar la divulgación de información de las AFP: indicadores de rentabilidad y riesgo para favorecer una toma de decisiones informada por parte del afiliado.</td>
                                          </tr> 
                                          <tr>
                                            <td valign="top">></td><td>Mejorar el gobierno corporativo y la gestión fiduciaria de las carteras de inversiones.</td>
                                          </tr> 
                                          <tr>
                                            <td valign="top">></td><td>Establecer reglas claras y adecuada tipificación de la conducta infractora para mejorar la capacidad de enforcement de la SBS.</td>
                                          </tr>                                           
                                        </table>                                    
                                    </p>
                                    <p>Estas importantes medidas redefinirán el mapa del mercado de capitales en los próximos años y esperamos que también se nutran durante su implementación  de los comentarios realizados por los 45 expositores, de los cuales 20 fueron extranjeros -incluyendo a los gerentes de inversiones de los fondos de pensiones más importantes de México, Colombia y Chile-, todos los cuales con su experiencia y conocimiento dieron sus puntos de vista y recomendaciones ante una multitudinaria audiencia.
                                    </p>                                    
