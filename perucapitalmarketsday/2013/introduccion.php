                        <p>
                            El Perú - Día del Mercado de Capitales 2012 promoverá el desarrollo del Mercado de Capitales Peruano a través del seminario internacional que se celebrará con este propósito en Lima, Perú, y que por segundo año consecutivo reunirá a los máximos representantes de las principales instituciones locales e internacionales que operan en estos mercados para discutir y promover el mejoramiento de la infraestructura, los reglamentos, la liquidez primaria y secundaria, la emisión de instrumentos de las nuevas inversiones, y las ideas innovadoras que están orientadas a transformar los mercados de capital peruanos en uno de las mejores plazas de la región latinoamericana para hacer negocios, y a su vez aprovechar las oportunidades de negocios que ofrece el Perú. En este sentido, será el mayor esfuerzo realizado a la fecha en el país para juntar la oferta de productos del mercado de capitales con la demanda proveniente de los inversionistas del mercado peruano.
                        </p>
