                        <h2 class="title">INTRODUCCIÓN</h2>
                        <p>
                          La Escuela de Postgrado de la Universidad del Pacífico y El Dorado Investments llevarán a cabo el 28 de setiembre el II Seminario Internacional Peru Capital Markets Day 2012. 
