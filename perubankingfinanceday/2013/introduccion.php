                    <section class="main-content left">
                        <h2 class="title">INTRODUCCIÓN</h2>
                        <p>
                            Con gran éxito llegó la tercera edición del Perú Banking &amp; Finance Day 2013 organizado por la Escuela de Postgrado de la Universidad del Pacífico y El Dorado Investments. El evento se realizó el pasado jueves 20 de junio en las instalaciones del Westin Lima Hotel &amp; Convention Center en San Isidro.
                        </p>
                        <p>
                            En esta oportunidad, nuestra decana, Elsa del Castillo, fue la encargada de inaugurar el evento junto con la bienvenida brindada por Melvin Escudero, CEO del Dorado Investments y Director de la Maestría en Finanzas con Especialización de Inversión de Portafolios.
                        </p>
                        <p>
                            Acto seguido, Luis Miguel Castilla, ministro de Economía del Perú, tuvo a su cargo la exposición de "Perspectivas de la economía peruana", en la cual dio cifras muy interesantes y ratificó su permanencia en la cartera de economía. Luego, Alberto Verme, Chairman of Europe, Middle Easte and Africa Banking del Citi, realizó la presentación titulada "El mundo, el Perú y su privilegio", en ella invita a los peruanos a sentirse orgullosos de haber nacido en esta tierra, y a pensar de que las cosas se pueden hacer en grande.
                        </p>       
                        <p>
                            El evento contó con seis paneles de alto nivel, que abordaron casos de éxito de empresas líderes en el ámbito nacional. El primer panel trató el tema "Impulsando el crecimiento y la expansión con la banca para la infraestructura (Caso de éxito: IC POWER)", el segundo panel fue sobre "Impulsando el crecimiento y la expansión con las microfinancieras (Caso de éxito: TOPITOP)", en el tercer panel se disertó sobre "Impulsando el crecimiento y la expansión con las tesorerías de los bancos (Caso de éxito: Backus)".
                        </p>
                        <p>
                            En el cuarto panel se discutió sobre "Impulsando el crecimiento y la expansión con la banca corporativa (Caso de éxito: INRETAIL Perú)"; en el quinto y último panel se expuso el tema "Impulsando el crecimiento y la expansión con la banca de inversión: fusiones, adquisiciones, estructuración de bonos (Caso de éxito: FALABELLA Perú)". Cabe indicar que durante el almuerzo Eduardo Torres Llosa, Director Gerente del BBVA dio la bienvenida como auspiciador del evento.
                        </p>
                        <p>
                            Luego de los paneles, Javier Poggy, representante de la Superintendencia de Banca y Seguros (SBS), dio a groso modo un panorama del sistema financiero nacional, en el cual destacó lo fielmente alineado que se encuentra el país a los estándares internacionales.
                        </p>
                        <p>
                            Llegando al final de la jornada, se presentó una mesa redonda con los CEOs más importantes de la banca peruana en el cual abordaron el tema: Los retos del sistema financiero. Por último, se entregó el "Premio a la Trayectoria de Vida por su contribución al desarrollo del sector financiero peruano", el cual fue designado en esta primera oportunidad a Dionisio Romero Seminario.
                        </p>
                        <p>
                            Así llegó a su fin la tercera edición del Perú Banking &amp; Finance Day, el cual reunió a lo más destacado del sector en el nivel nacional, en un evento que será realizado cada año con el fin de contribuir al desarrollo del país en temas de banca y finanzas.
                        </p>
