                                    </p>
                                    <p>
                                        Más tarde, entre otros espacios de reflexión, destacó una mesa redonda de gerentes generales de la banca privada que se ocupó de analizar los comportamientos de los grandes bloques de países, para resaltar la importancia de fortalecer al sistema financiero local y de trabajar por la inclusión financiera para estar mejor preparados y resistir los naturales cambios del mercado, producto de los ciclos de la economía.
                                    </p>        
                                    <p>
                                        En suma, el día se perfiló como una excelente oportunidad para replantearse la agenda de prioridades y para pensar en la importancia de ir consolidando el sector financiero peruano y sus instituciones, y donde se hace necesario también contar con servicios que respondan a las oportunidades de crecimiento empresarial y a una efectiva acción del Estado.                       
                                    </p>
