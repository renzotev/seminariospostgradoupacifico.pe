                                    <p>
                                        Como parte del Perú Banking & Finance Day 2013, realizado el jueves 20 de junio, se desarrolló una mesa redonda con los principales CEOs de la banca nacional, ellos abordaron el tema: Los retos del sistema financiero.
                                    </p>        

                                    <p>
                                        En la mesa participaron Luis Felipe Castellanos, Gerente General de Interbank; José Antonio Blanco, Gerente General de Citibank del Perú;  Jorge Ramos Felices, Gerente General de COFIDE; y  Esteban Hurtado, Gerente General del Banco Financiero. Además, Gonzalo Zegarra Mulanovich, Director Ejecutivo de Semana Económica fue el moderador.
                                    </p>    

                                    <p>
                                        Para Luis Felipe Castellanos es un momento interesante en el que se vive en la actualidad. "Creo que se viene repitiendo por un buen tiempo el tema de Europa y se contrapesa un poco con la expectativa de recuperación de Estados Unidos. Luego tenemos el tema de China, que sigue siendo una incógnita. Pero creo que toda esta coyuntura internacional nos agarra en un buen momento, tenemos muchas municiones como país para poder soportar esta crisis que empezó el 2008 y nunca paró", mencionó.
                                    </p>    

                                    <p>
                                        Además agrego que "las preocupaciones son las de mantener un sistema sólido, bien capitalizado, bien regulado, en crecimiento, rentable, y creo que con unos niveles de liquidez adecuado. Es muy importante ver que estamos en un país al cual le falta mucho por hacer en cuanto al desarrollo del sistema financiero. Entonces, las preocupaciones están orientadas en el sentí de qué medidas tomamos para mantener estos estándares y seguir incorporando a los millones de peruanos que lo necesitan", dijo.
                                    </p>    

                                    <p>
                                        Por su parte, José Antonio Blanco rescató la liquidez de los bancos. "Los últimos dos años han salido tanto las empresas a los mercados que los bancos estamos líquidos, se había reemplazado deuda por deuda internacional, entonces el efecto en el mercado local del sistema financiero no debería venir por un problema de liquidez".
                                    </p>    

                                    <p>
                                        "Por esto, queda claro que las consecuencias que más preocuparían serían que en el largo plazo haya un deterioro en las empresas o que la economía global, sobretodo la asiática, se deteriore a tal punto que a nosotros nos impacte significativamente", finalizó.
                                    </p>                                    

