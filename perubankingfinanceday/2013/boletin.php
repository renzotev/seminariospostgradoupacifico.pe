                                        La jornada se inició con la presentación del Ministro Economía, Luis Miguel Castilla, con importantes anuncios sobre la ya aprobada reforma del servicio civil, para luego destacar la necesidad de continuar fortaleciendo el crecimiento a partir de mercado interno, la promoción de la inversión y la reducción de la brecha de productividad del país.
                                    </p>
                                    <p>
