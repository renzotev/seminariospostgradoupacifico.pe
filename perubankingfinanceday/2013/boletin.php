                                        Hace pocos días llevamos a cabo la tercera versión del Perú Banking & Finance Day. Como en años anteriores, este foro congregó a varias centenas de profesionales y ejecutivos de las empresas más representativas del medio.
                                    </p>        
                                    <p>
                                        Algunos años antes nos habíamos comprometido con la organización de esta actividad para ir construyendo una comunidad de líderes empresariales y del Estado, de ejecutivos, de académicos y de profesionales que estudian en nuestra Escuela de Postgrado capaces de intercambiar experiencias, puntos de vista y, principalmente, de compartir sus visiones y expectativas sobre el futuro de nuestro país, con la intención de construir una necesaria mirada común. Tres años después, miramos con agrado que estamos contribuyendo a fortalecer este capital social que caracteriza a las naciones de mayor desarrollo.
